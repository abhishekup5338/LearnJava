package OOPSConcept;

public class StaticCoceptExample {
	
	
	/*There is two type of static method
	1. Static Method - In which there is mentioned static key word is known as static method.
	2. Non-static Method - In which there is no static key word is known as non-static method.
	3.We don't have to create object.
	*/
	
	public static void div()
	{
		System.out.println("this is static div");
	}
	
	//Our main method always a static method because JVM can call it without creating an object.

}
